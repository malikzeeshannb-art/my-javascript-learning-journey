console.log("5" == 5); // true because == checks for value only and performs type coercion
console.log("5" === 5); // false because === checks for both value and type
console.log(5 > 3); // true because 5 is greater than 3
console.log(5 < 3); // false because 5 is not less than 3
console.log("shani" === "shani"); // true because both value and type are the same
console.log("shani" === "Shani"); // false because of case sensitivity
